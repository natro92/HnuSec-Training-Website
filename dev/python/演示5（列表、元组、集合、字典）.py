#查
a = [1, 2, 3, 4, 5]
a # [1, 2, 3, 4, 5]
a[0] # 1
a[1:3] # [2, 3]
a[-1] # 5
a[-3:] # [3, 4, 5]
a[::2] # [1, 3, 5]
a[::-1] # [5, 4, 3, 2, 1]
a[::] # [1, 2, 3, 4, 5]

#增
a = [1, 2, 3]
a.append(4) # [1, 2, 3, 4]
a.insert(1, 5) # [1, 5, 2, 3, 4]
a.extend([6, 7]) # [1, 5, 2, 3, 4, 6, 7]
a + [8, 9] # [1, 5, 2, 3, 4, 6, 7, 8, 9]
a * 2 # [1, 5, 2, 3, 4, 6, 7, 1, 5, 2, 3, 4, 6, 7]

#删
a = [1, 2, 3, 4, 5]
a.pop() # [1, 2, 3, 4]
a.pop(1) # [1, 3, 4]
a.remove(3) # [1, 4]
del a[0] # [4]
a.clear() # []

#改
a = [1, 2, 3]
a[0] = 4 # [4, 2, 3]
a[1:3] = [5, 6] # [4, 5, 6]

