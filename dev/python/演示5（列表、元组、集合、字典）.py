a.clear() # {}

##生成式

#列表/元组/集合/字典
[i**2 for i in range(1,10)] # [1, 4, 9, 16, 25, 36, 49, 64, 81]
tuple(i**2 for i in range(1,10)) # (1, 4, 9, 16, 25, 36, 49, 64, 81)
{i for i in range(1,10)} # {1, 2, 3, 4, 5, 6, 7, 8, 9}
{a:b**2 for a,b in zip(range(1,10),range(9,0,-1))} # {1: 81, 2: 64, 3: 49, 4: 36, 5: 25, 6: 16, 7: 9, 8: 4, 9: 1}