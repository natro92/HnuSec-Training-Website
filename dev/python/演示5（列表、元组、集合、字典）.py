##集合
#定义
a = {1, 2, 3, 4, 5}
b = {1, 1, 2, 3, 3, 4, 5} # {1, 2, 3, 4, 5}
c = set() #空的集合

#部分操作
a = {1, 2, 3, 4, 5}
1 in a # True
6 not in a # True
a.add(6) # {1, 2, 3, 4, 5, 6}
a.remove(6) # {1, 2, 3, 4, 5}
a.clear() # {}

#一些集合操作
a = {1, 2, 3, 4, 5}
b = {4, 5, 6, 7, 8}
a | b # {1, 2, 3, 4, 5, 6, 7, 8} 并集
a & b # {4, 5} 交集
a - b # {1, 2, 3} 差集
a ^ b # {1, 2, 3, 6, 7, 8} 对称差

##字典
#定义
a = {"name": "张三", "age": 18}
b = dict()
c = {}

